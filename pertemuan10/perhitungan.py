import math
def tambah(b1,b2):
    hasil = b1+b2
    print("hasil dari pertambahan ",b1,"dan",b2," adalah",hasil)
def kurang(b1,b2):
    hasil = b1-b2
    print("hasil dari pengurangan ",b1,"dan",b2," adalah",hasil)
def pangkat(b1,b2):
    hasil = b1**b2
    print("hasil dari perpangkatan ",b1,"dan",b2," adalah",hasil)
def kali(b1,b2):
    hasil = b1*b2
    print("hasil dari perkalian ",b1,"dan",b2," adalah",hasil)
def bagi(b1,b2):
    hasil = b1/b2
    print("hasil dari pembagian ",b1,"dan",b2," adalah",hasil)
def log(b1):
    hasil = math.log(b1)
    print("hasil dari logaritma ",b1,"adalah",hasil)
def akar(b1):
    hasil = math.sqrt(b1)
    print("hasil dari akar",b1," adalah",hasil)
def sin(b1):
    hasil = math.sin(b1)
    print("hasil dari sin",b1," adalah",hasil)
def cos(b1):
    hasil = math.cos(b1)
    print("hasil dari cos",b1," adalah",hasil)
def tan(b1):
    hasil = math.tan(b1)
    print("hasil dari tan",b1," adalah",hasil)